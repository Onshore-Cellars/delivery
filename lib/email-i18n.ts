/**
 * Get a translated email string with variable interpolation
 */
export function te(key: string, locale: Locale = 'en', vars?: Record<string, string>): string {
  const lang = emailTranslations[locale] || emailTranslations.en
  let text = lang[key] || emailTranslations.en[key] || key

  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), v)
    }
  }

  return text
}

/**
 * Get locale from user's preferredLanguage field
 */
export function getUserLocale(preferredLanguage?: string | null): Locale {
  const supported: Locale[] = ['en', 'fr', 'es', 'it', 'el', 'nl', 'de', 'pt', 'tr', 'hr', 'ar']
  if (preferredLanguage && supported.includes(preferredLanguage as Locale)) {
    return preferredLanguage as Locale
  }
  return 'en'
}
